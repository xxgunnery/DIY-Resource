        <p>The Do-It-Yourself Resource Tripod is designed to replace a medium quality store-bought tripod. There were a number of design requirements that have been successfully implemented while keeping an easy assembly in mind.</p>
        <p>The first requirement set was to have telescoping legs. Telescoping legs allow your tripod to change its height, so that it meets any demand that you have. To meet this requirement, we use two different diameter PVC pipes, a 1/2" and 1" diameter. This difference in diameter allows the pipes to slide relative to each other and they can be locked in place by placing a screw in a set of pre-drilled holes. By optimizing leg spread and height settings, the tripod can change height by over a foot and a half.</p>
        <p>To accommodate cell phone users and camera users, camera and cell phone attachments were developed. The attachments are easy to change out and easy to make. The cell phone attachment features a phone clamp which holds your phone tightly while preventing the camera from being blocked. The camera attachment utilizes a screw with 20 threads-per-inch, which is a standard for cameras and allows you to fasten your camera tightly to the attachment.</p>
